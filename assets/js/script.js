function preguntar() {
  document.getElementById("puntaje").innerHTML =
    arrayMemoria2[arrayMemoria2.length - 1];

  const randomPregunta = parseInt(document.getElementById("random").innerHTML);

  opciones = document.formulario.color; //acceso a los botones radio
  for (i = 0; i < opciones.length; i++) {
    //buscar valor del botón pulsado
    if (opciones[i].checked == true) {
      elegido = opciones[i].value;
    }
  }

  switch (elegido) {
    //elegir color según boton pulsado
    case "esHombre":
      if (
        randomPregunta === 2 ||
        randomPregunta === 3 ||
        randomPregunta === 5 ||
        randomPregunta === 6 ||
        randomPregunta === 7 ||
        randomPregunta === 8 ||
        randomPregunta === 10
      ) {
        esMujer();
      } else {
        esHombre();
        const punto = 10;
        arrayMemoria.push(punto);
        const sumatoria =
          parseInt(arrayMemoria2[arrayMemoria2.length - 1]) +
          parseInt(arrayMemoria[arrayMemoria.length - 1]);
        arrayMemoria2.push(sumatoria);
        console.log(arrayMemoria);
        console.log(arrayMemoria2);
      }
      break;
    case "tieneGafas":
      if (
        randomPregunta === 1 ||
        randomPregunta === 3 ||
        randomPregunta === 4 ||
        randomPregunta === 5 ||
        randomPregunta === 6 ||
        randomPregunta === 11
      ) {
        tieneGafas();
      } else {
        notieneGafas();
        puntaje();
      }
      break;
    case "tieneSombrero":
      if (
        randomPregunta === 1 ||
        randomPregunta === 2 ||
        randomPregunta === 3 ||
        randomPregunta === 6 ||
        randomPregunta === 8 ||
        randomPregunta === 9
      ) {
        tieneSombrero();
        console.log("En IF es tieneSombrero", randomPregunta);
      } else {
        noTieneSombrero();
        puntaje();
      }
      break;
    case "orejasConejo":
      if (
        randomPregunta === 3 ||
        randomPregunta === 5 ||
        randomPregunta === 7 ||
        randomPregunta === 8
      ) {
        orejasConejo();
      } else {
        noOrejasConejo();
        puntaje();
      }
      break;
    case "tieneBigote":
      if (
        randomPregunta === 1 || 
        randomPregunta === 4 || 
        randomPregunta === 11
      ) {
        tieneBigote();
        console.log("En IF es orejasConejo", randomPregunta);
      } else {
        noTieneBigote();
        puntaje();
      }
      break;
    case "ojosGrades":
      ojosGrades();
      break;
    case "azul_marino":
      esJoven();
      break;
    default:
      //document.fgColor = "blue"
      break;
  }
}
